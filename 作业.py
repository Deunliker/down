import math  # 导入math模块，用于数学计算，例如平方根函数sqrt

def down(n):
    h = 100  # 初始高度为100米
    t = 0    # 初始化总时间为0秒
    L = 0    # 初始化总路程为0米

    # 循环n次，模拟小球落地和反弹的过程
    for i in range(n):
        # 计算小球从当前高度h下落到地面所需的时间，并累加到总时间t
        t += math.sqrt((2 * h) / 9.8)
        
        # 累加下落的路程到总路程L
        L += h
        
        # 小球反弹，高度减半
        h = h / 2
        
        # 计算小球从地面反弹到高度h所需的时间，并累加到总时间t
        t += math.sqrt((2 * h) / 9.8)
        
        # 累加反弹的路程到总路程L
        L += h

    # 返回最终的高度h、总时间t和总路程L
    return h, t, L

# 设置落地次数n为10
n = 10

# 调用down函数，获取第n次落地时的高度、总时间和总路程
h, t, L = down(n)

# 打印结果，保留两位小数
print(f"第{n}次落地时，高度为{h:.2f}m，时间为{t:.2f}秒，总路程为{L:.2f}m")